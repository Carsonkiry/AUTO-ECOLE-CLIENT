        <h2>Description :</h2>
        <br>
        
        <p>Le brevet de sécurité routière (BSR) correspond à la catégorie AM du permis de conduire. Il est obligatoire pour conduire un scooter de 50 cm3 ou une voiturette dès l'âge de 14 ans si vous êtes né en 1988 ou après. Pour obtenir le BSR, vous devez suivre une formation théorique validée par l'ASSR de 1er ou de 2nd niveau ou l'ASR. Vous devez aussi suivre une formation pratique d'une durée de 8 heures. La BSR obtenu à partir du 19 janvier 2013 a une durée de validité de 15 ans.</p>
       
       <br>
       
       <h2>Pièces à fournir :</h2>
        <br>
        
        <ul>
            <li>Une pièce d'identité en cours de validité</li>
            <li>Un justificatif de domicile de moins de 6 mois</li>
            <li>Une attestation de recensement pour les moins de 18 ans</li>
            <li>Une attestation de participation à la JAPD pour les personnes entre 18 et 25 ans</li>
            <li>Un code photo d’identité numérique</li>
        </ul>
